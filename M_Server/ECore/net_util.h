#pragma once

namespace ECore{

//-----------------------------------------------------------------------------
// 网络工具类
//-----------------------------------------------------------------------------
class ECORE_API NetUtil
{
public:
	//-------------------------------------------------------------------------
	// 初始化和关闭网络
	//-------------------------------------------------------------------------
	static BOOL			InitNetwork();
	static VOID			DestroyNetwork();

	//-------------------------------------------------------------------------
	// 得到本机名
	//-------------------------------------------------------------------------
	static std::string	GetHostName();

	//-------------------------------------------------------------------------
	// 根据机器名得到IP地址
	//-------------------------------------------------------------------------
	static BOOL			GetIPByHostName(DWORD& dwIP, LPCSTR szHostName);

	//-------------------------------------------------------------------------
	// 得到本机的广域网和局域网IP
	//-------------------------------------------------------------------------
	static BOOL			GetWANIP(DWORD& dwIP);
	static BOOL			GetLANIP(DWORD& dwIP);

	//-------------------------------------------------------------------------
	// 得到本机的MAC地址，可能会有多个
	//-------------------------------------------------------------------------
	static BOOL			GetMAC(list<UINT64>& listMAC);
	static BOOL			GetMacAddress(UINT64 &Mac);

	//-------------------------------------------------------------------------
	// 整型IP与字符串IP的相互转换
	//-------------------------------------------------------------------------
	static std::string	GetStringIP(DWORD dwIP);
	static DWORD		GetIntegerIP(LPCSTR szIP);
};

}