using System;
using System.Collections.Generic;
using System.Text;

namespace MonitorAndSet.TopoInfo
{
    /// <summary>
    /// MsDevice表示测量设备是否有数据等一系列信息
    /// </summary>
    public class MsDevice
    {
        public MsDevice()
        {

        }

        private int m_msdevid;
        /// <summary>
        /// 测量设备ID号
        /// </summary>
        public int msdevid
        {
            get { return m_msdevid; }
            set { m_msdevid = value; }
        }

        private String m_msdevcode;
        /// <summary>
        /// 测量设备代号
        /// </summary>
        public String msdevcode
        {
            get { return m_msdevcode; }
            set { m_msdevcode = value; }
        }

        private String m_msdevname;
        /// <summary>
        /// 测量设备型号
        /// </summary>
        public String msdevname
        {
            get { return m_msdevname; }
            set { m_msdevname = value; }
        }
        private int m_msdevtypeid;
        /// <summary>
        /// 测量设备类型ID号
        /// </summary>
        public int msdevtypeid
        {
            get { return m_msdevtypeid; }
            set { m_msdevtypeid = value; }
        }

        private String m_msdevtypename;
        /// <summary>
        /// 测量设备类型名
        /// </summary>
        public String msdevtypename
        {
            get { return m_msdevtypename; }
            set { m_msdevtypename = value; }
        }

        private bool m_state;
        /// <summary>
        /// 测量设备状态 为真表示有测量数据，为假表示没有测量数据
        /// </summary>
        public bool state
        {
            get { return m_state; }
            set { m_state = value; }
        }

        private String m_devIP;
        /// <summary>
        /// 组播IP地址
        /// </summary>
        public String devIP
        {
            get { return m_devIP; }
            set { m_devIP = value; }
        }

        private int m_devPort;
        /// <summary>
        /// 端口号
        /// </summary>
        public int devPort
        {
            get { return m_devPort; }
            set { m_devPort = value; }
        }

        private double m_Traffic;
        /// <summary>
        /// 流量(Byte/s)
        /// </summary>
        public double Traffic
        {
            get { return m_Traffic; }
            set { m_Traffic = value; }
        }
        
        private int m_XLocation;
        /// <summary>
        /// 拓扑图横坐标
        /// </summary>
        public int XLocation
        {
            get { return m_XLocation; }
            set { m_XLocation = value; }
        }

        private int m_YLocation;
        /// <summary>
        /// 拓扑图纵坐标
        /// </summary>
        public int YLocation
        {
            get { return m_YLocation; }
            set { m_YLocation = value; }
        }

        private string  m_RedBlueId;
        /// <summary>
        /// 红蓝标记1为红2为蓝 
        /// </summary>
        public string RedBlueId
        {
            get { return m_RedBlueId; }
            set { m_RedBlueId = value; }
        }

    }
}
